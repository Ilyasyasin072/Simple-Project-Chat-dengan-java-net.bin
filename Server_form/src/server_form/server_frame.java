    public class ServerStart implements Runnable 
    {
        @Override
        public void run() 
        {
            clientOutputStreams = new ArrayList();
            users = new ArrayList();  

            try 
            {
                ServerSocket serverSock = new ServerSocket(2222);//interface pada jaringan yang menjadi titik komunikasi antarmesin pada Internet Protocol, 
                //dan tentunya tanpa komunikasi ini, tidak akan ada pertukaran data dan informasi jaringan

                while (true) 
                {
				Socket clientSock = serverSock.accept();
				PrintWriter writer = new PrintWriter(clientSock.getOutputStream());
				clientOutputStreams.add(writer);

				Thread listener = new Thread(new ClientHandler(clientSock, writer));
				listener.start();
				ta_chat.append(" dapat conneksi connection. \n");
                }
            }
            catch (Exception ex)
            {
                ta_chat.append("koneksi erorr. \n");
            }
        }
    }
    
    public void userAdd (String data) //saat user connect maka tampilan ini akan mungcul
    {
        String message, add = ": :Connect", done = "Server: :Done", name = data;
        ta_chat.append("Sebelum " + name + " added. \n");
        users.add(name);
        ta_chat.append("Sesudah " + name + " added. \n");
        String[] tempList = new String[(users.size())];
        users.toArray(tempList);

        for (String token:tempList) 
        {
            message = (token + add);
            tellEveryone(message);
        }
        tellEveryone(done);
    }
    
    public void userRemove (String data) //menghapus user saat disconnect
    {
        String message, add = ": :Connect", done = "Server: :done", name = data;
        users.remove(name);
        String[] tempList = new String[(users.size())];
        users.toArray(tempList);

        for (String token:tempList) 
        {
            message = (token + add);
            tellEveryone(message);
        }
        tellEveryone(done);
    }
    
    public void tellEveryone(String message) 
    {
	Iterator it = clientOutputStreams.iterator();// saat user mendistkonneckan maka jika button mengirim send maka ada bacaan tersebut tampil

        while (it.hasNext()) 
        {
            try 
            {
                PrintWriter writer = (PrintWriter) it.next();
		writer.println(message);
		ta_chat.append("Sending: " + message + "\n");
                writer.flush();
                ta_chat.setCaretPosition(ta_chat.getDocument().getLength());

            } 
            catch (Exception ex) 
            {
		ta_chat.append("Error telling everyone. \n");
            }
        } 
    }
    
